DECLARE @jobId BINARY(16)
EXEC @ReturnCode =  msdb.dbo.sp_add_job @job_name=N'Cycle Error Log', 
		@enabled=1, 
		@notify_level_eventlog=2, 
		@notify_level_email=0, 
		@notify_level_netsend=0, 
		@notify_level_page=0, 
		@delete_level=0, 
		@description=N'Prevents the SQL Server error log from getting too large. Recycles on a weekly schedule - Sunday at 12:00 AM', 
		@category_name=N'DBA Admin', 
